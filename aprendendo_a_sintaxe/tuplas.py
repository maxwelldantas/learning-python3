# Entendendo sobre tuplas

# Declaração de Listas do tipo Tuplas são imutáveis
# por convenção coloca-se o sinal de parenteses abrindo e fechando um tupla (porém é opcional)
# quando temos uma lista que sabemos que não iremos modificar seus valores, usamos as tuplas
dias_da_semana = ('Domingo', 'Segunda', 'Terça', 'Quarta', 'Quinta', 'Sexta', 'Sábado')

dias_da_semana_2 = 'Domingo', 'Segunda', 'Terça', 'Quarta', 'Quinta', 'Sexta', 'Sábado'

print(type(dias_da_semana))
print(type(dias_da_semana))


